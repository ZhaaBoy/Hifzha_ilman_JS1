let hari = "Rabu";
switch (hari) {
  case "Senin":
    console.log("Hari ini adalah Senin.");
    break;
  case "Selasa":
    console.log("Hari ini adalah Selasa.");
    break;
  case "Rabu":
    console.log("Hari ini adalah Rabu.");
    break;
  default:
    console.log("Hari ini bukan hari kerja.");
}
